

public class Tri2 {
    public static void main(String[] args) {
        for(int i=0;i<4;i++){
            for(int j=4-i;j>0;j--){
                System.out.print(" ");
            }
                for(int k=0;k<i+1;k++){
                    System.out.print("* "); 
                    
            }
            System.out.println("");
                
        }
       
            
      
         for(int i=0;i<3;i++){
            for(int j=0;j<=i;j++){
                System.out.print(" ");
            }
                for(int k=3-i;k>0;k--){
                    System.out.print(" *"); 
                    
            }
            System.out.println("");
                
        
        
    
   

    
}
}
}
